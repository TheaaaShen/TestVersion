package util;

public class ProbInfo {
    int strucID;
    double strucProb;
    double finalProb;
    public void setStruID(int strucID)
    {
        this.strucID=strucID;
    }
    public void setStruProb(double strucProb)
    {
        this.strucProb=strucProb;
    }
    public int getStruID()
    {
        return this.strucID;
    }
    public double getStruProb()
    {
        return this.strucProb;
    }
    public void setFinalProb(double finalProb)
    {
        this.finalProb=finalProb;
    }
    public double getFinalProb()
    {
        return this.finalProb;
    }
    

}
